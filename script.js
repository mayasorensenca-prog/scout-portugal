const CURRENCIES = {
  EUR: { symbol:'€', rate:1 },
  USD: { symbol:'$', rate:1.09 },
  GBP: { symbol:'£', rate:0.86 },
  CNY: { symbol:'¥', rate:7.85 },
  BRL: { symbol:'R$', rate:5.80 },
  AUD: { symbol:'A$', rate:1.65 }
};

let lang = 'en';
let currency = 'EUR';

function get(obj, path) { return path.split('.').reduce((o, k) => o?.[k], obj); }

function applyLang(l) {
  lang = l;
  document.documentElement.lang = l;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = get(T[l], el.dataset.i18n);
    if (val !== undefined) el.textContent = val;
  });
}

function applyCurrency(c) {
  currency = c;
  const { symbol, rate } = CURRENCIES[c];
  document.querySelectorAll('.cur-sym').forEach(el => el.textContent = symbol);
  document.querySelectorAll('.amount[data-eur]').forEach(el => {
    const base = parseInt(el.dataset.eur, 10);
    el.textContent = Math.round(base * rate).toLocaleString();
  });
}

/* NAV SCROLL + SCROLL PROGRESS + PARALLAX */
const nav = document.getElementById('nav');
nav.classList.add('scrolled');
const scrollBar = document.getElementById('scroll-progress');
const heroVideoWrap = document.querySelector('.hero-video-wrap');
window.addEventListener('scroll', () => {
  const sy = window.scrollY;
  nav.classList.toggle('scrolled', sy > 24);
  if (scrollBar) {
    const pct = sy / (document.documentElement.scrollHeight - window.innerHeight) * 100;
    scrollBar.style.width = pct + '%';
  }
  if (heroVideoWrap) heroVideoWrap.style.transform = 'translateY(' + (sy * 0.3) + 'px)';
}, { passive: true });

/* BURGER */
const burger = document.getElementById('burger');
const mobileMenu = document.getElementById('mobile-menu');
burger.addEventListener('click', () => {
  const open = mobileMenu.classList.toggle('open');
  burger.classList.toggle('open', open);
  burger.setAttribute('aria-expanded', open);
});
mobileMenu.querySelectorAll('a').forEach(a => a.addEventListener('click', () => {
  mobileMenu.classList.remove('open');
  burger.classList.remove('open');
  burger.setAttribute('aria-expanded', false);
}));

/* SMOOTH SCROLL */
document.querySelectorAll('a[href^="#"]').forEach(a => {
  a.addEventListener('click', e => {
    const target = document.querySelector(a.getAttribute('href'));
    if (!target) return;
    e.preventDefault();
    window.scrollTo({ top: target.getBoundingClientRect().top + window.scrollY - nav.offsetHeight, behavior: 'smooth' });
  });
});

